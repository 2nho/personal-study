package kr.co.goodee39.date1025;

public class Ex02Variable1 {

	public static void main(String[] args) {
		// 자료형
		/* - Java 에서 쓰이는 기본 데이터의 타입을 의미한다.
		 * - 타입은 크게 다섯가지로 정수형,실수형,논리형,문자형,문자열형으로 나뉜다.
		 * - 정수형 : -3,-2,-1,0,1,2,3...
		 * - 실수형 : 0.3, 3.12, -0.23...
		 * - 논리형(불리언형) : true , false
		 * - 문자형 : 'a', '가', '※'...
		 * - 문자열형 : "abcde","가나다라마","안녕하세요"...
		 * */
		// 정수형
		System.out.println(3);
		System.out.println(-23);
		System.out.println(0);
		
		// 실수형
		System.out.println(0.45);
		System.out.println(72.33);
		System.out.println(-0.548);
		System.out.println(0.248e+5);
		System.out.println(127e-4);
		
		// 논리형
		System.out.println(true);
		System.out.println(false);
		
		// 문자형
		System.out.println('1');
		System.out.println('가');
		System.out.println('＠');
		System.out.println('a');
		
		// 문자열형
		System.out.println("안녕하세요");
		System.out.println("hello java");
		System.out.println("12345");
		System.out.println("-1-2-3-4-5");
		System.out.println("내가\t 이겼다");
		System.out.println("비내리는\n호남선");
	}

}
